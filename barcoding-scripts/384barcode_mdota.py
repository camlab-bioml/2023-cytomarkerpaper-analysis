def custom_transfer(volume, source, destination, pipette):
    #pipette.pick_up_tip()
    pipette.aspirate(volume, source.bottom(z=1.5))
    #protocol.delay(1)
    pipette.touch_tip()

    pipette.air_gap(2)
    pipette.dispense(volume+5, destination.top(-3))
    #protocol.delay(1)
    pipette.blow_out()
    pipette.touch_tip(radius=0.15, v_offset=-1, speed=80)
    pipette.touch_tip(radius=0.15, v_offset=-1, speed=80)
    pipette.touch_tip(radius=0.15, v_offset=-1, speed=80)
    pipette.touch_tip(radius=0.15, v_offset=-1, speed=80)
    #pipette.drop_tip()
        
def custom_batch(volume, source, barcode_list, pipette, destination):
    
    pipette.pick_up_tip()
